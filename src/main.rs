

fn main() {
    println!("Hello, World");

    let mut x = 5;
    println!("The value of x is: {}", x);

    x = 6;
    println!("The value of x is: {}", x);

    let a = 5;
    let a = a + 1;
    let a = a * 2;

    println!("The value of a is: {}", a);

    let spaces = " ";
    let spaces = spaces.len();

    println!("The value of spaces is: {}", spaces);
}